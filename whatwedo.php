          <h4 class="service-heading" style="padding-top: 2%;">Ugiri Autonomous Community</h4>
          <p class="text-muted">Ugiri is an autonomous community located in the southern part of Isiala Mbano Local Government Area, Imo State, Nigeria. Ugiri community comprises 7 villages, namely Ugirinna, Umuneke, Oka, Ogbor, Ibeme, Obollo, and Umuozu. The Ugiri people share similar cultural heritage with other groups and areas in Imo State and Igbo speaking people of Nigeria.</p>
          <p class="text-muted"> The founding fathers of this community group were part of the core members of Mbano Community Association (MCA). </p>
          <p class="text-muted">Many years ago, some members in MCA formed sub-community groups based on the villages they came from. This included Ehime, Amuzari, etc.Not to be out-done, a 
            group of three men from Ugiri, notably Sam Ozurumba, Chief Obi Nwagwu and Chief Kieran Eke got together and decided form a group which would now be known as
            Nzuko ndi Ugiri. They made phone calls to invite people from Ugiri to join this group. </p>
          <p class="text-muted">The first meeting was held at Chief Obi Nwagwu’s office. Initially, meetings were held on ad hoc basis before it was then agreed to meet on a two monthly
            basis. Chief Obi Nwagwu continued to host the meetings, including the provision of snacks for a long time. It was only when he moved office to a much smaller
            premises, and it was not viable to hold the meeting in the new place, that a decision was made to rotate the meetings from one member’s house to another. 
          </p>
        </div>
        <div class="col-md-4" style="min-width: fit-content; padding-top: 4%;">
          <span class="fa-stack fa-4x">
            <img class="rounded-circle img-fluid" src="img/ukmap.png" width="75%">
          </span>
          <h4 class="service-heading" style="padding-top: 2%;">Nzuko Ndi Ugiri UK & Ireland</h4>
          <p class="text-muted">Nzuko Ndi Ugiri UK & Ireland is a non-profit organisation formed in the year 2000, and was officially launched in London in May 2007. The organisation is open to all indigenes of Ugiri residing in the United Kingdom and the Republic of Ireland.</p>
        </div>        
      </div>
